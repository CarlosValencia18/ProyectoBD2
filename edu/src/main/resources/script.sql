--------------------------------------------------------
--  DDL for Function CALCULAR_CALIFICACION
--------------------------------------------------------

  CREATE OR REPLACE NONEDITIONABLE FUNCTION "PROYECTOBD2"."CALCULAR_CALIFICACION" (
    p_examen_presentado_id IN VARCHAR2
) RETURN NUMBER IS
    v_calificacion_ponderada NUMBER := 0;
    v_calificacion_simple NUMBER := 0;
    v_total_ponderado NUMBER := 0;
    v_total_simple NUMBER := 0;
BEGIN
    -- Calcular la calificación para preguntas con porcentaje asignado
    SELECT COALESCE(SUM(CASE WHEN r.es_correcta = 'Y' THEN ep.porcentaje ELSE 0 END), 0)
    INTO v_calificacion_ponderada
    FROM respuestas_estudiantes re
    JOIN examenes_preguntas ep ON re.preguntas_id_pregunta = ep.preguntas_id_pregunta
    JOIN opciones_pregunta r ON re.opciones_pregunta_id_opcion = r.id_opcion
    WHERE re.exam_presentados_id_exam_pres = p_examen_presentado_id
      AND ep.porcentaje IS NOT NULL;

    -- Calcular el total ponderado (suma de porcentajes de preguntas con porcentaje asignado)
    SELECT COALESCE(SUM(ep.porcentaje), 0)
    INTO v_total_ponderado
    FROM examenes_preguntas ep
    WHERE ep.examenes_id_examen = (
        SELECT examenes_id_examen
        FROM examenes_presentados
        WHERE id_examen_presentado = p_examen_presentado_id
    )
      AND ep.porcentaje IS NOT NULL;

    -- Calcular el número de preguntas sin porcentaje asignado
    SELECT COALESCE(COUNT(*), 0)
    INTO v_total_simple
    FROM respuestas_estudiantes re
    JOIN examenes_preguntas ep ON re.preguntas_id_pregunta = ep.preguntas_id_pregunta
    WHERE re.exam_presentados_id_exam_pres = p_examen_presentado_id
      AND ep.porcentaje IS NULL;

    -- Calcular el puntaje para preguntas sin porcentaje asignado
    SELECT COALESCE(SUM(CASE WHEN r.es_correcta = 'Y' THEN 1 ELSE 0 END), 0)
    INTO v_calificacion_simple
    FROM respuestas_estudiantes re
    JOIN examenes_preguntas ep ON re.preguntas_id_pregunta = ep.preguntas_id_pregunta
    JOIN opciones_pregunta r ON re.opciones_pregunta_id_opcion = r.id_opcion
    WHERE re.exam_presentados_id_exam_pres = p_examen_presentado_id
      AND ep.porcentaje IS NULL;

    -- Calcular la calificación total
    IF v_total_simple > 0 THEN
        v_calificacion_simple := (v_calificacion_simple / v_total_simple) * (100 - v_total_ponderado);
    ELSE
        v_calificacion_simple := 0;
    END IF;

    RETURN 5 * (v_calificacion_ponderada + v_calificacion_simple)/100;
END calcular_calificacion;

