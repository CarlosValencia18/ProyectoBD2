--  DDL for Procedure SP_OBTENER_PREGUNTAS_EXAMEN
--------------------------------------------------------
set define off;

  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "PROYECTOBD2"."SP_OBTENER_PREGUNTAS_EXAMEN" (
    p_examen_id IN VARCHAR2,
    p_result OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
    WITH preguntas_examen AS (
        SELECT p.id_pregunta, p.texto_pregunta, p.duracion, ep.porcentaje, p.privada, 
               p.preguntas_id_pregunta, p.tipo_pregunta_id_tipo, op.id_opcion, op.texto_opcion, 
               op.es_correcta
        FROM preguntas p
        LEFT JOIN opciones_pregunta op ON p.id_pregunta = op.preguntas_id_pregunta
        JOIN examenes_preguntas ep ON p.id_pregunta = ep.preguntas_id_pregunta
        WHERE ep.examenes_id_examen = p_examen_id
    ),
    preguntas_aleatorias AS (
        SELECT p.id_pregunta, p.texto_pregunta, p.duracion, 0 AS porcentaje, p.privada, 
               p.preguntas_id_pregunta, p.tipo_pregunta_id_tipo, op.id_opcion, op.texto_opcion, 
               op.es_correcta
        FROM preguntas p
        LEFT JOIN opciones_pregunta op ON p.id_pregunta = op.preguntas_id_pregunta
        JOIN preguntas_bancos pb ON p.id_pregunta = pb.preguntas_id_pregunta
        JOIN bancos_de_preguntas bp ON pb.bancos_de_preguntas_id_banco = bp.id_banco_preguntas
        JOIN examenes e ON e.temas_id_tema = bp.temas_id_tema
        WHERE e.id_examen = p_examen_id
          AND p.id_pregunta NOT IN (
              SELECT p.id_pregunta
              FROM preguntas p
              JOIN examenes_preguntas ep ON p.id_pregunta = ep.preguntas_id_pregunta
              WHERE ep.examenes_id_examen = p_examen_id
          )
        ORDER BY DBMS_RANDOM.VALUE
        FETCH FIRST (SELECT num_preguntas_aleatorias FROM examenes WHERE id_examen = p_examen_id) ROWS ONLY
    )
    SELECT * FROM preguntas_examen
    UNION ALL
    SELECT * FROM preguntas_aleatorias;
END sp_obtener_preguntas_examen;

