  SELECT 
    ep.exam_asignados_exam_id_exam AS idExamen,
    re.Preguntas_id_pregunta AS idPregunta,
    es.id_estudiante AS idUsuario,
    e.nombre AS nombreExamen,
    p.texto_pregunta AS pregunta,
    op.texto_opcion AS respuesta,
    tp.tipo_pregunta AS TipoPregunta,
    CASE
        WHEN op.es_correcta = 'Y' THEN 'Correcta'
        ELSE 'Incorrecta'
    END AS clasificacion
