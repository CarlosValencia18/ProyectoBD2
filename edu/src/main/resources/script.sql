  CREATE OR REPLACE FORCE NONEDITIONABLE VIEW "PROYECTOBD2"."VW_NOTAS_ESTUDIANTE" ("ID_ESTUDIANTE", "NOMBRE_ESTUDIANTE", "APELLIDO_ESTUDIANTE", "ID_EXAMEN", "NOMBRE_EXAMEN", "FECHA_PRESENTACION", "CALIFICACION", "TIEMPO", "DIRECCION_IP") AS 
  SELECT
    es.id_estudiante,
    es.nombres AS nombre_estudiante,
    es.apellidos AS apellido_estudiante,
    e.id_examen,
    e.nombre AS nombre_examen,
    ep.fecha_presentacion,
    ep.calificacion,
    ep.tiempo_tomado AS Tiempo,
    ep.direccion_ip
FROM
    Estudiantes es
JOIN
    Examenes_Presentados ep ON es.id_estudiante = ep.Estudiantes_id_estudiante
JOIN
    Examenes e ON ep.exam_asignados_exam_id_exam = e.id_examen
;
