--  DDL for View VW_PREGUNTAS_PUBLICAS
--------------------------------------------------------

  CREATE OR REPLACE FORCE NONEDITIONABLE VIEW "PROYECTOBD2"."VW_PREGUNTAS_PUBLICAS" ("ID_PREGUNTA", "TEXTO_PREGUNTA", "BANCO_PREGUNTAS", "TEMA", "TIPO_PREGUNTA") AS 
  SELECT
    p.id_pregunta,
    p.texto_pregunta,
    bp.nombre AS banco_preguntas,
    t.nombre AS tema,
    tp.tipo_pregunta
FROM
    Preguntas p
JOIN
    Preguntas_Bancos pb ON p.id_pregunta = pb.Preguntas_id_pregunta
JOIN
    Bancos_de_Preguntas bp ON pb.Bancos_de_Preguntas_id_banco = bp.id_banco_preguntas
JOIN
    Temas t ON bp.Temas_id_tema = t.id_tema
JOIN
    Tipo_Pregunta tp ON p.Tipo_Pregunta_id_tipo = tp.id_tipo
WHERE
    p.privada = 'N'
;
